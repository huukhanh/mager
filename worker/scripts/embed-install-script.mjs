#!/usr/bin/env node
// Bakes scripts/install.sh into the Worker bundle so /install.sh has no
// runtime dependency on raw.githubusercontent.com — which doesn't work
// for private repos.
//
// Output: worker/src/embed/install-script.ts (gitignored, regenerated each deploy).

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, "..", "..");
const src = resolve(repoRoot, "scripts", "install.sh");
const out = resolve(here, "..", "src", "embed", "install-script.ts");

const text = readFileSync(src, "utf8");

const banner = `// AUTO-GENERATED by worker/scripts/embed-install-script.mjs.
// Source: scripts/install.sh — edit there, not here. Regenerated on deploy.
`;

const body = `export const INSTALL_SCRIPT_SOURCE = ${JSON.stringify(text)};\n`;

mkdirSync(dirname(out), { recursive: true });
writeFileSync(out, banner + body);

const sizeKb = (text.length / 1024).toFixed(1);
console.log(`embed-install-script: wrote ${out} (${sizeKb}KB)`);
